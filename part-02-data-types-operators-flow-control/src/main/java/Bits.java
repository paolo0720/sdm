void main() {
    int x = 0b00000000000000000000000000010110;
    int y = 0b00000000000000000000000000110011;

    IO.println(x & y);         // 00000000000000000000000000010010
    IO.println(x | y);         // 00000000000000000000000000110111
    IO.println(x ^ y);         // 00000000000000000000000000100101
    IO.println(~x);            // 11111111111111111111111111101001

    x = 0b00000000000000000000000000001001; // 9
    IO.println(x >> 3);        // 00000000000000000000000000000001
    IO.println(x >>> 3);       // 00000000000000000000000000000001

    x = -9;                    // 11111111111111111111111111110111
    IO.println(x >> 3);        // 11111111111111111111111111111110
    IO.println(x >>> 3);       // 00011111111111111111111111111110
}

